    private void startFetchThread()
    {
        //Thread A -> UserList empfangen
        new Thread(new Runnable() {
            @Override
            public void run() {
                //pr√ºft die event queue auf den aktuellen Thread.isInterrupted()
                while (!Thread.interrupted()) {
                    if (client != null) {
                        try {
                            
                            client.updateUserlist();
                        } catch (IOException ex) {
                            Logger.getLogger(RN_A2.class.getName()).log(Level.SEVERE, null, ex);
                        }
                      
                        updateUsers(client.getUsers());
                    }
                    try {
                        Thread.sleep(5000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(RN_A2.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            }
        }).start();
    }
    
    public void setClient(Client pClient) throws IOException {
        this.client = pClient;
        client.setCallback(new ICallback() {
            @Override
            public void call(final String message) {
                java.awt.EventQueue.invokeLater(new Runnable() {
                    @Override
                    public void run() {
                        appendUserMessage(message);
                    }
                });
            }
        });
        startFetchThread();
    }
    
    public Client getClient()
    {
        return this.client;
    }

    private void appendUserMessage(String message) {
        jta_chatverlauf.append(message + '\n');
    }

    public void updateUsers(List<String> users) {
        DefaultListModel<String> model = (DefaultListModel<String>) jl_userlist.getModel();
        model.clear();
        for (String elem : users) {
            model.addElement(elem);
        }
    }

